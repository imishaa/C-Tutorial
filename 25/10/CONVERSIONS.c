#include <stdio.h>

int main()
{
    int x=3,y=6;
    char z='D';

    printf("Octal and hexadecimal values are: %o\v",x);
    printf("%X\n",x);
    printf("Decimal and octal values are: %d\v",y);
    printf("%o\n",y);
    printf("Decimal and hexadecimal values are: %d\v",z);
    printf("%X\n",z);  
      
    return 0;
}
