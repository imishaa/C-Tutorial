#include <stdio.h>

int main()
{
    short int a=5;
    int b=7;
    long int c=54635;
    long long int d=98899999;
    float e=9.7;
    double f=9.76888867;
    long double g=72.45267;

    printf("The value for int is %d\n",a);
    printf("The value for short int is %hd\n",b);
    printf("The value for long int is %ld\n",c);
    printf("The value for long long int is %lld\n",d);
    printf("The value for float is %f\n",e);
    printf("The value for double is %lf\n",f);
    printf("The value for long double is %Lf\n",g);






    return 0;
}
